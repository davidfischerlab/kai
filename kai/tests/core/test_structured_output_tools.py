    def create_basic_context(self, **kwargs) -> ExecutionContext:
        """Create basic execution context for tool testing."""
        context_fields = {
            "current_cell": "",
            "error_message": "",
            "execution_history": [],
            "conversation_history": [],
            "notebook_structure": {
                "totalCells": 3,
                "allCells": ["# Cell 1", "# Cell 2", "# Cell 3"]
            },
            "last_execution_failed": False,
            "autonomous_mode": True,
            "last_cell_modified_in_auto_mode": kwargs.get("last_cell_modified_in_auto_mode", None),
            **kwargs
        }

        exec_inputs = ExecutionInputs(
            user_query=kwargs.get("user_query", "Test query"),
            context=context_fields,
            task_list=kwargs.get("task_list", {"tasks": []}),
            backtracking_context=kwargs.get("backtracking_context", None)
        )

        return ExecutionContext(
            inputs=exec_inputs,
            session_metadata={"session_id": "test"}
        )

