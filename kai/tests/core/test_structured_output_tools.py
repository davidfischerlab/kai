    @pytest.mark.asyncio
    @pytest.mark.parametrize("update_rule", ["KEEP", "UPDATE"])
    async def test_autonomous_update_tasks_tool_update_rule(self, llm_interface, update_rule):
        """Test AutonomousUpdateTasksTool KEEP vs UPDATE logic with mocked responses."""
        from unittest.mock import Mock, AsyncMock
        from kai.core.orchestration.schemas import AutonomousTaskUpdate, TaskItem

        tool = AutonomousUpdateTasksTool(llm_interface)

        # Original task list with completed and active tasks
        original_task_list = {
            "tasks": [
                {"id": 1, "task": "Load data", "status": "completed"},
                {"id": 2, "task": "Process data", "status": "completed"},
                {"id": 3, "task": "Analyze data", "status": "active"},
                {"id": 4, "task": "Generate plots", "status": "pending"}
            ]
        }

        context = self.create_basic_context(
            task_list=original_task_list,
            rag_enabled=True,  # Enable RAG to test query handling
            last_execution_failed=False
        )

        # Mock structured response based on update_rule
        if update_rule == "KEEP":
            # KEEP should preserve original task list
            mock_result = AutonomousTaskUpdate(
                tasks=[
                    TaskItem(id=3, task="Analyze data", status="active"),
                    TaskItem(id=4, task="Generate plots", status="pending")
                ],
                retrieval_queries=["test query"],
                update_rationale="Keeping existing task structure as no changes needed",
                update_rule="KEEP"
            )
            expected_task_count = 4  # All original tasks preserved
            expected_completed_count = 2  # Original completed tasks preserved
        else:
            # UPDATE should apply new task structure
            mock_result = AutonomousTaskUpdate(
                tasks=[
                    TaskItem(id=3, task="Enhanced data analysis", status="active"),
                    TaskItem(id=4, task="Create visualizations", status="pending"),
                    TaskItem(id=5, task="Write summary report", status="pending")
                ],
                retrieval_queries=["enhanced analysis"],
                update_rationale="Updated task list to include enhanced analysis and reporting",
                update_rule="UPDATE"
            )
            expected_task_count = 5  # 2 completed + 3 new tasks
            expected_completed_count = 2  # Original completed tasks preserved

        # Mock the LLM provider to return our controlled response
        original_generate_structured = tool.llm_provider.generate_structured
        tool.llm_provider.generate_structured = AsyncMock(return_value=mock_result)

        try:
            result = await tool.execute(context)

            # Validate result structure
            assert result is not None
            assert hasattr(result, 'output_workflow')
            assert hasattr(result, 'output_ui')

            # Check workflow output
            workflow_output = result.output_workflow
            assert "task_list" in workflow_output

            # Parse the updated task list
            updated_task_list = workflow_output["task_list"]
            updated_tasks = updated_task_list["tasks"]

            # Validate task count
            assert len(updated_tasks) == expected_task_count, f"Expected {expected_task_count} tasks, got {len(updated_tasks)}"

            # Count completed tasks (should always preserve completed ones)
            completed_tasks = [t for t in updated_tasks if t["status"] == "completed"]
            assert len(completed_tasks) == expected_completed_count, f"Expected {expected_completed_count} completed tasks, got {len(completed_tasks)}"

            # Validate completed tasks are preserved with original IDs
            if expected_completed_count > 0:
                assert completed_tasks[0]["id"] == 1
                assert completed_tasks[0]["task"] == "Load data"
                assert completed_tasks[1]["id"] == 2
                assert completed_tasks[1]["task"] == "Process data"

            if update_rule == "KEEP":
                # For KEEP: verify exact preservation of original task list
                assert updated_tasks == original_task_list["tasks"]
                print(f"KEEP test passed: Task list preserved exactly")
            else:
                # For UPDATE: verify completed tasks preserved + new tasks added
                non_completed_tasks = [t for t in updated_tasks if t["status"] != "completed"]
                assert len(non_completed_tasks) == 3  # 3 new tasks from mock
                assert non_completed_tasks[0]["task"] == "Enhanced data analysis"
                assert non_completed_tasks[1]["task"] == "Create visualizations"
                assert non_completed_tasks[2]["task"] == "Write summary report"
                print(f"UPDATE test passed: Completed tasks preserved, new tasks added")

            # Check RAG query handling
            if mock_result.retrieval_queries:
                assert "snippet_retrieval_query" in workflow_output
                assert workflow_output["snippet_retrieval_query"] == mock_result.retrieval_queries

            print(f"AutonomousUpdateTasksTool ({update_rule}) validation successful")

        except Exception as e:
            pytest.fail(f"AutonomousUpdateTasksTool ({update_rule}) failed: {e}")
        finally:
            # Restore original method
            tool.llm_provider.generate_structured = original_generate_structured

